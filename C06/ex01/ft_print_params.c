#include <unistd.h>

int	main(int ac, char **av)
{
	int	a;
	int	i;

	a = 1;
	i = 0;
	while (a < ac)
	{
		i = 0;
		while (av[a][i])
		{
			write(1, &av[a][i], 1);
			i++;
		}
		write(1, "\n", 1);
		a++;
	}
	return (0);
}
