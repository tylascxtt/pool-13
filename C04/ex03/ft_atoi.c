int	ft_atoi(char *str)
{
	int	i;
	int	c;
	int	nb;

	i = 0;
	nb = 0;
	c = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			nb++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = (c * 10) + (str[i] - '0');
		i++;
	}
	if (nb % 2 == 1)
		return (c * (-1));
	else
		return (c);
}
